pub fn score(s: []const u8) u32 {
    var sum: u32 = 0;
    for (0..s.len) |i| {
        switch (s[i]) {
            97,
            101,
            105,
            111,
            117,
            108,
            110,
            114,
            115,
            116,
            65,
            69,
            73,
            79,
            85,
            76,
            78,
            82,
            83,
            84,
            => sum += 1,
            100, 103, 68, 71 => sum += 2,
            98, 99, 109, 112, 66, 67, 77, 80 => sum += 3,
            102, 104, 118, 119, 121, 70, 72, 86, 87, 89 => sum += 4,
            107, 75 => sum += 5,
            106, 120, 74, 88 => sum += 8,
            113, 122, 81, 90 => sum += 10,
            else => sum += 0,
        }
    }
    return sum;
}
